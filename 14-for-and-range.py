# Es posible crear un rango de números sucesivos utilizando la función range () e iterarla.

#Con un solo argumento, range parte desde el cero hasta el entero inferior  
for num in range(6):
    print (num)

print ("-------------------")

# Con dos argumentos entrega un listado a partir del primer número (incluido) hasta el entero inferior 
for num in range (1,6):
    print (num)

# Ejemplo en la terminal si es (6) significa que es del 0 al 5 osea 0 - 1 - 2 - 3 - 4 - 5 y si es (1,6) parte del 1 al 5 osea 1 - 2 -3 - 4 - 5.